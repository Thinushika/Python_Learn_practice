#Escape sequences
### \\  --- Backslash(\)
print("Escape character: \\ a \\")

### \'  --- single quote(')
print("Escape character: \' a \'")

### \"  --- Double quote(")
print("Escape character: \" a \"")

### \a  --- ASCII bell(BEL)
print("Escape character: \a a")

### \b  --- ASCII backspace(BS)
print("Escape character: \b a")

### \f  --- ASCII formfeed(FF)
print("Escape character: \f a")

### \n  --- ASCII linefeed(LF)
print("Escape character: \n a")

### \t  --- Horizontal tab(TAB)
print("Escape character: \t a")

### \v  --- ASCII vertical tab(VT)
print("Escape character: \v a")
